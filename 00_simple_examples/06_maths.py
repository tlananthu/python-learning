number1=int(input('Please input a number'))
number2=int(input('Please input another number'))

operation=input('Please input an operation. +-/*')

if operation=='+':
    print(number1+number2)
elif operation=='-':
    print(number2-number2)
elif operation=='/':
    print(number2/number2)
elif operation=='*':
    print(number2*number2)